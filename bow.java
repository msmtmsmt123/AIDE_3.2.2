class bow {
    static final bnv DW;
    static final bnt j6;

    static {
        j6 = new bpi();
        DW = new bpk();
    }

    static bnt j6(bne bne) {
        return bne.j6() < 1 ? j6 : new bpt(bne);
    }

    static bnv DW(bne bne) {
        return bne.j6() < 1 ? DW : new bpu(bne);
    }
}

import java.util.BitSet;

class aim implements ajp {
    BitSet j6;

    public aim(BitSet bitSet) {
        this.j6 = bitSet;
    }

    public void j6(aja aja) {
        if (!ail.DW(aja)) {
            this.j6.set(aja.Ws().VH());
        }
    }

    public void j6(ajd ajd) {
        if (!ail.DW(ajd)) {
            this.j6.set(ajd.Ws().VH());
        }
    }

    public void DW(aja aja) {
        agp Ws = aja.Ws();
        if (!ail.DW(aja) && Ws != null) {
            this.j6.set(Ws.VH());
        }
    }
}
